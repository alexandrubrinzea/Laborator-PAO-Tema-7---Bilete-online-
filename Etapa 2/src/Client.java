public class Client {
    private String firstName, lastName, emailAdress;
    int age;

    public Client() {
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getEmailAdress() {
        return emailAdress;
    }

    public int getAge() {
        return age;
    }


}
